package model;

public class Email {
	private String from, to, fromPassword, contennt, subbject;

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getFromPassword() {
		return fromPassword;
	}

	public void setFromPassword(String fromPassword) {
		this.fromPassword = fromPassword;
	}

	public String getContennt() {
		return contennt;
	}

	public void setContennt(String contennt) {
		this.contennt = contennt;
	}

	public String getSubbject() {
		return subbject;
	}

	public void setSubbject(String subbject) {
		this.subbject = subbject;
	}
	

}
